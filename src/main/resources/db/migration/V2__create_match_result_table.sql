CREATE TABLE match_results
(
    ID               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SEASON           VARCHAR(5) NOT NULL,
    MATCH_DATE       DATE       NOT NULL,
    HOME_TEAM_ID     BIGINT     NOT NULL,
    GUEST_TEAM_ID    BIGINT     NOT NULL,
    HOME_TEAM_GOALS  INT2       NOT NULL,
    GUEST_TEAM_GOALS INT2       NOT NULL,
    CONSTRAINT HOME_TEAM FOREIGN KEY (HOME_TEAM_ID) REFERENCES teams (ID) ON DELETE CASCADE ,
    CONSTRAINT GUEST_TEAM FOREIGN KEY (GUEST_TEAM_ID) REFERENCES teams (ID) ON DELETE CASCADE
);